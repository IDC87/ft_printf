#include "ft_printf.h"

int ft_putchar(char c)
{
    write(1, &c, 1);
    return(1);
}

size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}

int ft_putstr(const char *s)
{
    write (1, s, ft_strlen(s));
    return (ft_strlen(s));
} 

char	*ft_strchr(const char *s, int c)
{
	while (*s != (unsigned char)c)
		if (!*s++)
			return (0);
	return ((char *)s);
}